
# Operaciones matematicas

a, b = 2,3

suma = a + b
print("suma =",suma)

resta = a - b
print("resta =",resta)

mul = a * b
print("mul =",mul)

div = a / b
print("div =",div)

div_ent = a // b
print("div_ent =",div_ent)

potencia = a ** b
print("potencia =",potencia)

resto = a % b
print("resto =",resto)
